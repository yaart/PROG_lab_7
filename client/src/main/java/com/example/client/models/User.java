package com.example.client.models;

/**
 * Класс, представляющий пользователя системы.
 * <p>
 * Содержит базовую информацию о пользователе — имя пользователя и пароль.
 * Может использоваться для аутентификации, передачи данных между клиентом и сервером,
 * а также в тестовых или служебных целях.
 * </p>
 */
public class User {
    /**
     * Имя пользователя.
     * <p>Уникальный идентификатор пользователя в системе.</p>
     */
    public String username;

    /**
     * Пароль пользователя.
     * <p>Следует учитывать безопасность при передаче и хранении этого поля.</p>
     */
    public String pass;

    /**
     * Конструктор по умолчанию, необходимый для сериализации/десериализации.
     */
    public User() {
    }

    /**
     * Конструктор для создания объекта пользователя с заданными учетными данными.
     *
     * @param username имя пользователя, не должно быть null
     * @param pass     пароль пользователя, может быть null (не рекомендуется)
     */
    public User(String username, String pass) {
        this.username = username;
        this.pass = pass;
    }
}